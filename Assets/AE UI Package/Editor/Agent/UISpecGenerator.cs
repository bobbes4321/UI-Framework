        private static GameObject CreateChild(RectTransform parent, string name, out RectTransform rect)
        {
            var go = new GameObject(name, typeof(RectTransform));
            rect = (RectTransform)go.transform;
            rect.SetParent(parent, worldPositionStays: false);
            return go;
        }

        private static void Stretch(RectTransform rect)
        {
            rect.anchorMin = Vector2.zero;
            rect.anchorMax = Vector2.one;
            rect.offsetMin = Vector2.zero;
            rect.offsetMax = Vector2.zero;
        }

        private static T ParseEnum<T>(string value, T fallback, GenerateReport report, string context) where T : struct
        {
            if (string.IsNullOrEmpty(value)) return fallback;
            if (Enum.TryParse(value, ignoreCase: true, out T parsed)) return parsed;
            report.issues.Add($"Unknown {context} '{value}', using {fallback}");
            return fallback;
        }

        private static UIMoveDirection ParseDirection(string value, GenerateReport report)
        {
            if (string.IsNullOrEmpty(value)) return UIMoveDirection.CustomPosition;
            if (Enum.TryParse(value, ignoreCase: true, out UIMoveDirection direction)) return direction;
            report.issues.Add($"Unknown move direction '{value}', using CustomPosition");
            return UIMoveDirection.CustomPosition;
        }

        private static float ParseFloat(string value, float fallback) =>
            float.TryParse(value ?? "", System.Globalization.NumberStyles.Float,
                System.Globalization.CultureInfo.InvariantCulture, out float f) ? f : fallback;

        private static bool ParseBool(string value) =>
            string.Equals(value, "True", StringComparison.OrdinalIgnoreCase) || value == "1";

        private static Vector3 ParseVector3(string value, Vector3 fallback)
        {
            if (string.IsNullOrEmpty(value)) return fallback;
            string[] parts = value.Split(',');
            if (parts.Length == 3
                && float.TryParse(parts[0], System.Globalization.NumberStyles.Float, System.Globalization.CultureInfo.InvariantCulture, out float x)
                && float.TryParse(parts[1], System.Globalization.NumberStyles.Float, System.Globalization.CultureInfo.InvariantCulture, out float y)
                && float.TryParse(parts[2], System.Globalization.NumberStyles.Float, System.Globalization.CultureInfo.InvariantCulture, out float z))
                return new Vector3(x, y, z);
            return fallback;
        }

        private static Vector3 ParseScale(string value, Vector3 fallback)
        {
            if (string.IsNullOrEmpty(value)) return fallback;
            if (float.TryParse(value, System.Globalization.NumberStyles.Float,
                    System.Globalization.CultureInfo.InvariantCulture, out float uniform))
                return new Vector3(uniform, uniform, 1f);
            return ParseVector3(value, fallback);
        }

        private static string Sanitize(string value)
        {
            foreach (char invalid in Path.GetInvalidFileNameChars())
                value = value.Replace(invalid, '_');
            return value;
        }

        private static void EnsureFolder(string path)
        {
            if (AssetDatabase.IsValidFolder(path)) return;
            string parent = Path.GetDirectoryName(path)?.Replace('\\', '/');
            string leaf = Path.GetFileName(path);
            if (!string.IsNullOrEmpty(parent) && !AssetDatabase.IsValidFolder(parent)) EnsureFolder(parent);
            AssetDatabase.CreateFolder(parent, leaf);
        }
