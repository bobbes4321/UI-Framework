        private static void GenerateFlow(FlowSpec flowSpec, GenerateReport report)
        {
            EnsureFolder($"{GeneratedRoot}/Flow");
            string path = $"{GeneratedRoot}/Flow/{Sanitize(flowSpec.name)}.asset";

            var graph = AssetDatabase.LoadAssetAtPath<FlowGraph>(path);
            bool created = graph == null;
            if (!created && (graph.graphDescription == null || !graph.graphDescription.Contains("[generated")))
            {
                report.collisions.Add($"'{path}' exists but was not generated — flow '{flowSpec.name}' skipped");
                return;
            }
            if (created) graph = ScriptableObject.CreateInstance<FlowGraph>();

            graph.graphName = flowSpec.name;
            graph.graphDescription = GeneratedFlowTag;
            graph.nodes.Clear();

            var start = graph.AddNode<StartNode>("Start", new Vector2(0f, 0f));
            if (!string.IsNullOrEmpty(flowSpec.start))
                start.outputs.Add(new FlowEdge { portName = "Start", toNode = flowSpec.start, allowsBack = false });

            for (int i = 0; i < flowSpec.nodes.Count; i++)
            {
                FlowNodeSpec nodeSpec = flowSpec.nodes[i];
                var node = graph.AddNode<UINode>(nodeSpec.name, new Vector2(280f * (i + 1), 120f * (i % 3)));
                node.hideShownViewsOnExit = true;
                foreach (string shownView in nodeSpec.views)
                {
                    CategoryNameId.Parse(shownView, out string category, out string name);
                    node.showViews.Add(new UINode.ViewRef(category, name));
                }
                foreach (string hiddenView in nodeSpec.hide)
                {
                    CategoryNameId.Parse(hiddenView, out string category, out string name);
                    node.hideViews.Add(new UINode.ViewRef(category, name));
                }
                foreach (FlowEdgeSpec edgeSpec in nodeSpec.next)
                {
                    node.outputs.Add(new FlowEdge
                    {
                        portName = edgeSpec.trigger != null ? edgeSpec.trigger.ToString() : "Next",
                        toNode = edgeSpec.to,
                        allowsBack = edgeSpec.allowsBack,
                        trigger = edgeSpec.trigger
                    });
                }
            }

            graph.startNode = "Start";
            List<string> graphIssues = graph.Validate();
            foreach (string issue in graphIssues) report.issues.Add($"Flow '{flowSpec.name}': {issue}");

            if (created) AssetDatabase.CreateAsset(graph, path);
            EditorUtility.SetDirty(graph);
            (created ? report.created : report.updated).Add($"Flow graph '{flowSpec.name}' → {path}");
        }

