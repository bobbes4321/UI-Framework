        private static void GeneratePopup(PopupSpec popupSpec, AEUISettings settings, GenerateReport report)
        {
            EnsureFolder($"{GeneratedRoot}/Popups");
            string path = $"{GeneratedRoot}/Popups/{Sanitize(popupSpec.name)}.prefab";

            GameObject existing = AssetDatabase.LoadAssetAtPath<GameObject>(path);
            bool created = existing == null;
            if (!created && existing.GetComponent<GeneratedMarker>() == null)
            {
                report.collisions.Add($"'{path}' exists but was not generated — popup '{popupSpec.name}' skipped");
                return;
            }

            GameObject root;
            if (popupSpec.elements.Count > 0)
            {
                root = BuildRichPopup(popupSpec, settings, report);
            }
            else
            {
                root = UIWidgetFactory.CreatePopup(popupSpec.name, popupSpec.title, popupSpec.message);
                if (popupSpec.size != null && popupSpec.size.Length >= 2)
                    root.GetComponent<UIPopup>().content.sizeDelta =
                        new Vector2(popupSpec.size[0], popupSpec.size[1]);
            }
            if (popupSpec.close) UIWidgetFactory.CreatePopupCloseButton(root, popupSpec.name);
            GeneratedMarker marker = root.AddComponent<GeneratedMarker>();
            marker.specSource = $"popup:{popupSpec.name}";

            GameObject prefab = PrefabUtility.SaveAsPrefabAsset(root, path);
            UnityEngine.Object.DestroyImmediate(root);

            if (settings.popupDatabase != null)
            {
                settings.popupDatabase.AddOrUpdate(popupSpec.name, prefab);
                EditorUtility.SetDirty(settings.popupDatabase);
            }
            (created ? report.created : report.updated).Add($"Popup '{popupSpec.name}' → {path}");
        }

        /// <summary>
        /// Popup with spec-authored content: optional Title/Message labels followed by the element
        /// tree, all stacked in the card content. The UIPopup indexed slots (labels/images/buttons)
        /// fill in build order so the Doozy-style runtime data APIs (SetTexts/SetSprites/SetEvents)
        /// address the generated content.
        /// </summary>
        private static GameObject BuildRichPopup(PopupSpec popupSpec, AEUISettings settings, GenerateReport report)
        {
            Vector2 cardSize = popupSpec.size != null && popupSpec.size.Length >= 2
                ? new Vector2(popupSpec.size[0], popupSpec.size[1])
                : UIWidgetFactory.PopupDefaultCardSize;
            GameObject root = UIWidgetFactory.CreatePopupShell(popupSpec.name, cardSize, out RectTransform content);
            UIPopup popup = root.GetComponent<UIPopup>();

            if (!string.IsNullOrEmpty(popupSpec.title))
                popup.labels.Add(UIWidgetFactory.CreateLabel(content, popupSpec.title,
                    UIWidgetFactory.TokenTextStrong, 28f, "Title", textStyle: UIWidgetFactory.TextStyleHeading));
            if (!string.IsNullOrEmpty(popupSpec.message))
                popup.labels.Add(UIWidgetFactory.CreateLabel(content, popupSpec.message,
                    UIWidgetFactory.TokenTextDefault, 22f, "Message", textStyle: UIWidgetFactory.TextStyleBody));

            var build = new ViewBuild { root = root };
            for (int i = 0; i < popupSpec.elements.Count; i++)
                BuildElementTree(popupSpec.elements[i], i, content, settings, report, inLayout: true, build: build);
            ResolveTabPanels(build, settings, report);

            popup.images.AddRange(build.imageSlots);
            popup.buttons.AddRange(build.buttonSlots);
            return root;
        }

        private static void AddViewCommand(GameObject go, string viewId, ViewCommandOnClick.Command command)
        {
            if (string.IsNullOrEmpty(viewId)) return;
            CategoryNameId.Parse(viewId, out string category, out string name);
            ViewCommandOnClick viewCommand = go.AddComponent<ViewCommandOnClick>();
            viewCommand.command = command;
            viewCommand.view = new ViewId(category, name);
        }

        private static void AddLabel(RectTransform parent, string label, string colorToken)
        {
            GameObject labelGo = CreateChild(parent, "Label", out RectTransform labelRect);
            Stretch(labelRect);
            TextMeshProUGUI text = labelGo.AddComponent<TextMeshProUGUI>();
            text.text = label ?? "";
            text.alignment = TextAlignmentOptions.Center;
            text.fontSize = 32f;
            if (!string.IsNullOrEmpty(colorToken)) AddThemeTarget(labelGo, colorToken);
        }

        private static void AddThemeTarget(GameObject go, string token)
        {
            ThemeColorTarget target = go.AddComponent<ThemeColorTarget>();
            target.token = token;
        }

        private static void ApplyPresetByName(string presetName, UIAnimation animation,
            AEUISettings settings, GenerateReport report, string context)
        {
            if (string.IsNullOrEmpty(presetName)) return;
            UIAnimationPreset preset = settings.animationPresets != null ? settings.animationPresets.Get(presetName) : null;
            if (preset == null)
            {
                report.issues.Add($"{context}: preset '{presetName}' not found (define it in the spec's presets section)");
                return;
            }
            preset.CopyTo(animation);
        }

